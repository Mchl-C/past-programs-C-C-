int check(string word,char input){
    bool found = false;
    string result;
    for(int i = 0;i < word.size();i++){
        if(word[i] == input){
            found = true;
            found_word[i] = word[i];
        }
    }
    if(!found){
        step ++;
    }
    if(step >= 8){
        run = false;
        win = false;
    }
    for(int s = 0;s < word.size();s++){
        result += found_word[s];
    }
    if(result == word){
        run = false;
        win = true;
    }
    showDisplay(step);
}


